/*
==============
Bola ping-pong
==============

Di dalam sebuah kotak A ada 50 bola ping-pong.
Setiap bola ping pong bisa berwarna merah atau putih.

Buatlah pseudocode untuk menghitung jumlah
bola ping-pong berwarna merah dan putih masing-masing di dalam kotak A.

===== jawaban =====

SET countMerah = 0
SET countPutih = 0
SET warnaBola = [50 value of either merah or putih]

FOR bola = 1 to 50
    IF warnaBola = merah THEN
        CALCULATE countMerah + 1
    ELSE IF warnaBola = putih THEN
        CALCULATE countPutih + 1
END FOR
DISPLAY countMerah and countPutih

*/